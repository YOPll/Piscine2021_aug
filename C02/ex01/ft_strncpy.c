char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char			*keep;
	unsigned int	i;

	i = 0;
	keep = dest;
	while (*src && i < n)
	{
		*dest++ = *src++;
		i++;
	}
	while (i < n)
	{
		*dest++ = '\0';
		i++;
	}
	return (keep);
}
