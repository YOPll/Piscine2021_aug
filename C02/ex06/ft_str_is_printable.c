int	ft_str_is_printable(char *str)
{
	int	i;

	i = 1;
	while (*str)
	{
		if (*str >= 32 && *str != 127)
			i = 1;
		else
		{
			i = 0;
			break ;
		}
		str++;
	}
	return (i);
}
